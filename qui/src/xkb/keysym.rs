macro_rules! named_int_enum {
    (
        $(#[$outer:meta])*
        $v:vis enum $EnumName:ident : $Type:ty {
            $(
                $(#[$inner:meta])*
                $Variant:ident = $Value:expr => $Name:expr,
            )+
        }
        utf32 {
            $(
                $UtfKey:expr => $UtfValue:expr,
            )*
        }
    ) => {
        $(#[$outer])*
        $v enum $EnumName {
            $(
                $(#[$inner])*
                $Variant,
            )+
        }

        impl $EnumName {
            pub fn name(&self) -> &'static str {
                match self {
                    $(
                        $EnumName::$Variant => $Name,
                    )+
                }
            }

            pub fn from_name(name: &str) -> Option<Self> {
                match name {
                    $(
                        $Name => Some($EnumName::$Variant),
                    )+
                    _ => None,
                }
            }

            pub fn to_utf32(self) -> Option<u32> {
                let keysym: $Type = self.into();
                let keysym_u32 = keysym as u32;

                if (0x0020..=0x007e).contains(&keysym_u32) || (0x00a0..=0x00ff).contains(&keysym_u32) {
                    return Some(keysym_u32);
                }

                if keysym_u32 == 0xff80 {
                    return Some(0x20);
                }

                if ((0xff08..=0xff0b).contains(&keysym_u32))
                    || ((0xffaa..=0xffb9).contains(&keysym_u32))
                    || keysym_u32 == 0xff0d
                    || keysym_u32 == 0xff1b
                    || keysym_u32 == 0xffff
                    || keysym_u32 == 0xff89
                    || keysym_u32 == 0xff8d
                    || keysym_u32 == 0xffbd
                {
                    return Some(keysym_u32 & 0x7f);
                }

                if (0x0100d800..=0x0100dfff).contains(&keysym_u32) {
                    return None;
                }

                if (0x01000000..=0x0110ffff).contains(&keysym_u32) {
                    return Some(keysym_u32 - 0x01000000);
                }

                if (0x10081200..=0x10081209).contains(&keysym_u32) {
                    return Some(keysym_u32 - 0x10081200 + 0x30);
                }

                match keysym_u32 {
                    0x1008120a => return Some(0x2a),
                    0x1008120b => return Some(0x23),
                    _ => {}
                }

                match keysym_u32 {
                    $(
                        $UtfKey => Some($UtfValue),
                    )*
                    _ => None,
                }
            }

            pub fn to_char(self) -> Option<char> {
                self.to_utf32().and_then(char::from_u32)
            }
        }

        impl ::std::convert::TryFrom<$Type> for $EnumName {
            type Error = String;

            fn try_from(value: $Type) -> Result<Self, String> {
                #[allow(unreachable_patterns)]
                match value {
                    $(
                        $Value => Ok($EnumName::$Variant),
                    )+
                    i => Err(format!("{:?} is not a valid {}", i, stringify!($EnumName))),
                }
            }
        }

        impl From<&$EnumName> for $Type {
            fn from(e: &$EnumName) -> $Type {
                match e {
                    $(
                        $EnumName::$Variant => $Value,
                    )+
                }
            }
        }

        impl From<$EnumName> for $Type {
            fn from(e: $EnumName) -> $Type {
                match e {
                    $(
                        $EnumName::$Variant => $Value,
                    )+
                }
            }
        }

        impl ::bytestruct::Size for $EnumName {
            fn size(&self) -> usize {
                let val: $Type = self.into();
                val.size()
            }
        }

        impl ::bytestruct::ReadFromWithEndian for $EnumName {
            fn read_from_with_endian<T: ::std::io::Read>(source: &mut T, endian: ::bytestruct::Endian) -> ::std::io::Result<Self> {
                let val = <$Type>::read_from_with_endian(source, endian)?;

                #[allow(unreachable_patterns)]
                match val {
                    $(
                        $Value => Ok($EnumName::$Variant),
                    )+
                    _ => Err(::std::io::Error::new(
                        ::std::io::ErrorKind::InvalidData,
                        format!("invalid value for {}: {}", stringify!($EnumName), val),
                    )),
                }
            }
        }

        impl ::bytestruct::WriteToWithEndian for $EnumName {
            fn write_to_with_endian<W: ::std::io::Write>(&self, writer: &mut W, endian: ::bytestruct::Endian) -> ::std::io::Result<()> {
                let val: $Type = match self {
                    $(
                        $EnumName::$Variant => $Value,
                    )+
                };

                val.write_to_with_endian(writer, endian)
            }
        }
    };
}

